<h1>Hello FRAME WORK</h1>
<p>Hello ramework adalah struktur nyata atau konseptual yang dimaksudkan berfungsi sebagai pendukung atau panduan membangun dan memperluas struktur menjadi sesuatu yang bermanfaat.</p>
<p>Ketika mengacu pada software komputer, pengertian framework adalah fondasi dengan tingkat kerumitan yang ditentukan dan dapat diperluas oleh pemrogram dengan menggunakan kode mereka sendiri termasuk kompiler, juru bahasa, atau API. Secara umum, framework menyediakan lingkungan yang memfasilitasi jenis pemrograman tertentu untuk proyek pengembangan software.</p>
<p>Beberapa framework sistem komputer juga termasuk program aktual, menentukan interface pemrograman, atau menawarkan alat pemrograman untuk menggunakan kerangka kerja. Suatu framework mungkin digunakan untuk sekumpulan fungsi dalam suatu sistem dan bagaimana semuanya saling terkait..</p>
